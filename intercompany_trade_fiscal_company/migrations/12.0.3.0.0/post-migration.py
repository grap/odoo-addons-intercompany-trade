from openupgradelib import openupgrade


@openupgrade.migrate()
def migrate(env, version):
    openupgrade.logged_query(
        env.cr,
        """
        UPDATE account_journal
        SET is_intercompany_trade_fiscal_company = true
        WHERE code in ('ACTI', 'VTTI');
        """,
    )
